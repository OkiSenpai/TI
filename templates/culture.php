            <h3>Architecture</h3>
            <p>Amsterdam possède une riche histoire architecturale dont l'une des meilleures illustrations est le
                bâtiment
                le plus ancien de la ville, la Oude Kerk (« vieille église »), située au cœur du quartier de De Wallen
                et
                qui a été consacrée en 1306. Le plus vieux bâtiment en bois remonte à 1425 ; il s'agit de la Houten Huys
                («
                maison de bois » en ancien néerlandais) qui se trouve dans le Begijnhof. Il s'agit de l'un des deux
                seuls
                bâtiments en bois encore présents à Amsterdam et de l'un des rares exemples toujours visibles
                d'architecture
                gothique. En effet, les bâtiments en bois, trop vulnérables aux flammes, ont été rasés au XVIe siècle
                pour
                laisser place à des matériaux non inflammables. À cette même époque, de nombreux bâtiments sont
                construits
                dans le style architectural Renaissance. Les bâtiments de cette période sont très reconnaissables à
                leurs
                façades à pignons à redents, caractéristique de la Renaissance néerlandaise. Amsterdam développe même
                rapidement sa propre architecture Renaissance, qui repose sur les principes de l'architecte Hendrick de
                Keyser, à l'image de la Westerkerk, conçue selon ses plans. Au XVIIe siècle, l'architecture baroque
                devient
                très populaire, comme à travers toute l'Europe, grâce notamment aux architectes Jacob van Campen,
                Philips
                Vingboons et Daniel Stalpaert. Philips Vingboons conçoit notamment de splendides maisons de négociants à
                travers toute la ville.
                <br><br>
                Largement influencée par la culture française, l'architecture baroque se développe fortement tout au
                long du
                XVIIIe siècle comme en témoigne le Palais Royal sur le Dam. Vers 1815, les architectes rompent avec le
                style
                baroque et commencent à construire des bâtiments de style néogothique. À la fin du XIXe siècle, le style
                Art
                nouveau devient à la mode et beaucoup d'architectes optent pour ce nouveau style très populaire. Du fait
                de
                la très forte expansion de la ville d'Amsterdam à cette époque, beaucoup de bâtiments arborent ce style
                à
                proximité du centre-ville ou autour de Museumplein. Le style Art déco, et sa variante locale de l'École
                d'Amsterdam, se développent au cours de la première moitié du XXe siècle, notamment dans le quartier du
                Rivierenbuurt. L'une des caractéristiques notables du style de l'École d'Amsterdam est l'utilisation de
                façades très décorées et fleuries, avec des fenêtres et des portes de formes irrégulières.

                <br><br>
                Le vieux centre-ville constitue donc un vaste creuset qui regroupe tous les styles architecturaux
                d'avant la
                fin du XIXe siècle. Les styles Art déco et géorgien se retrouvent quant à eux principalement à
                l'extérieur
                du centre-ville dans les quartiers construits au début du XXe siècle. La majorité des bâtiments
                historiques
                du centre-ville est constituée de bâtiments à pignons, dont les grandes maisons de marchands qui bordent
                les
                canaux constituent la meilleure illustration. Les façades à pignons de formes différentes signent les
                architectures de chaque époque.
            </p>

            <h3>Canaux concentriques</h3>
            <p>Le système de canaux d'Amsterdam est le fruit d'une politique d'urbanisme réfléchie au début du XVIIe
                siècle.
                Un plan complet fut élaboré, basé sur quatre demi-cercles concentriques de canaux, dont les extrémités
                émergent dans la baie de l'IJ. Trois canaux principaux, le Herengracht, le Keizersgracht et le
                Prinsengracht, furent construits pour le développement résidentiel, formant la "courbure d'or". Le
                quatrième
                canal, le Singelgracht, est plus périphérique.
                <br><br>
                Les canaux servaient à la défense militaire, à la gestion de l'eau et au transport. La construction
                commença
                en 1613 et se fit d'ouest en est. Les travaux de la dernière portion du canal entre le Leidsegracht et
                l'Amstel débutèrent entre 1658 et 1662, mais ne furent pas totalement terminés en 1679. La partie
                orientale
                du réseau de canaux ne fut jamais réalisée.
                <br><br>
                Les canaux d'Amsterdam, surnommée la "Venise du Nord", s'étendent sur plus de cent kilomètres, avec
                environ
                1 700 ponts reliant quatre-vingt-dix îles. Les canaux sont reliés par d'autres perpendiculaires, comme
                le
                Brouwersgracht et le Leidsegracht. En 2010, les canaux d'Amsterdam obtinrent le label de patrimoine
                mondial
                sous l'intitulé "Zone des canaux concentriques du XVIIe siècle à l'intérieur du Singelgracht".
            </p>
            <h3>Vie culturelle</h3>
            <p>
                Au cours de la dernière partie du XVIe siècle, les Rederijkerskamers (« chambres de rhétorique »)
                d'Amsterdam, à l'image de De Egelantier, organisent des concours entre les différentes chambres de
                lecture
                de poésie et de théâtre. La création de l'Académie en 1617 permet à Amsterdam de compter les cercles
                littéraires les plus réputés des Provinces-Unies au XVIe siècle. En 1637, Amsterdam bâtit son premier
                théâtre, conçu par Jacob van Campen, où des spectacles de ballet sont donnés dès 1642. Au XVIIIe siècle,
                le
                théâtre français devient populaire. Il y a peu de productions nationales d'opéra au cours du XIXe siècle
                alors qu'Amsterdam est sous l'influence de la musique allemande. Le Hollandse Opera est construit en
                1888
                pour y remédier et promouvoir l'opéra néerlandais. À cette époque, la culture populaire est centrée
                autour
                du vaudeville et du music-hall autour de la zone Nes à Amsterdam. Le métronome, l'une des avancées les
                plus
                importantes de la musique classique européenne, est inventé en 1812 à Amsterdam par Dietrich Nikolaus
                Winkel. À la fin de ce siècle, le Rijksmuseum, Stedelijk et le Concertgebouw sont construits. Avec le
                XXe
                siècle arrivent le cinéma, la radio et la télévision. Bien que la plupart des studios soit situés à
                Hilversum et Aalsmeer, la programmation est largement influencée par Amsterdam où vivent beaucoup de
                gens
                qui travaillent dans l'industrie de la télévision.
                <br><br>
                Le zoo d'Amsterdam, Artis, tient son nom de la Société royale de zoologie Natura Artis Magistra (« La
                Nature
                est maîtresse de l'Art »). Il est l'un des plus anciens du monde (le bâtiment principal date de 1838),
                avec
                celui de Londres (1828). Situé en plein centre-ville, son ambiance contraste fortement avec l'agitation
                urbaine environnante. Il comporte un aquarium (bâti en 1882), des musées zoologique et géologique, un
                planétarium ainsi qu'une bibliothèque universitaire.
                <br><br>
                La bibliothèque centrale d'Amsterdam possède des locaux centraux récents : ils sont gagnés sur l'eau,
                près
                de la gare, dans le quartier du Oostelijk Havengebied. Elle est ouverte au public et gratuite. Le marché
                aux
                fleurs de la ville, présente différentes fleurs venant des champs néerlandais. Visité en masse par les
                touristes étrangers, qui achètent le plus souvent des bulbes à emporter, le marché possède également ses
                habitués, qui viennent y acheter des fleurs à bas coût.
            </p>
            <h3>Musées</h3>
            <p>
                Les musées les plus importants d'Amsterdam se trouvent sur la place du Musée (Museumplein), créée à la
                fin
                du XIXe siècle. Le Rijksmuseum, au nord de la place, possède la plus grande collection d'art classique
                néerlandais, avec des œuvres de Rembrandt, Vermeer et d'autres maîtres du XVIIe siècle. Le musée Van
                Gogh,
                au nord-ouest, expose des toiles célèbres de Van Gogh comme Les Tournesols et La Chambre de Van Gogh à
                Arles. À côté, le Stedelijk Museum, inauguré en 1895, abrite des œuvres d'art moderne de Mondrian, Appel
                et
                Malevitch.
                <br><br>

                Amsterdam accueille également de nombreux autres musées. Le Nederlands Scheepvaartmuseum possède la plus
                riche collection consacrée à la marine. L'Amsterdam Museum retrace l'histoire de la ville, tandis que la
                Maison Anne Frank attire des milliers de visiteurs. Le musée historique juif occupe quatre synagogues
                ashkénazes, et le Bijbels Museum contient la première Bible imprimée en Hollande. Le Verzetsmuseum
                retrace
                la vie sous l'occupation nazie, et la synagogue portugaise est ouverte à la visite.
                <br><br>
                Parmi les autres musées, la maison de Rembrandt reconstitue la vie de l'artiste, et l'Hermitage est une
                dépendance du musée de l'Ermitage de Saint-Pétersbourg. Le Tropenmuseum est consacré à l'ethnographie,
                et le
                Cabinet des Chats présente des œuvres dédiées aux chats. Le FOAM, musée de la photographie, et le
                Nederlands
                Filmmuseum sont consacrés aux arts visuels et au cinéma. Le EYE Film Instituut Nederland a déménagé à
                Amsterdam-Noord en 2012.
            </p>
            <h3>Musique</h3>
            <p> Amsterdam possède un orchestre symphonique de renommée mondiale, l'Orchestre royal du Concertgebouw, qui
                évolue au sein du Concertgebouw situé sur la Museumplein. L'acoustique de cette salle de concert est
                considérée par la critique comme l'une des meilleures du monde. Le bâtiment contient trois salles : la
                grande salle, la petite salle et la galerie des glaces. Près de huit cents concerts y sont produits
                chaque
                année, avec une fréquentation d'environ 850 000 spectateurs. L'opéra d'Amsterdam, le Muziektheater, est
                quant à lui situé à côté de l'hôtel de ville au sein du même ensemble architectural surnommé « Stopera »
                (mot-valise issu de Stadhuis, « hôtel de ville », et d'opéra). Cet immense complexe moderne, ouvert en
                1986,
                se situe dans l'ancien quartier juif de Waterlooplein près de la rivière Amstel. Il héberge les troupes
                du
                Nederlandse Opera, du Nationale Ballet et du Holland Symfonia. Ouvert en 2005, le Muziekgebouw aan 't IJ
                est
                une salle de concert située sur l'IJ, au nord de la gare centrale qui accueille principalement des
                représentations de musique contemporaine. Situé à proximité immédiate, le Bimhuis est plutôt consacré au
                jazz et à l'improvisation.
                <br><br>
                Le Heineken Music Hall est une salle de concert située près de la Johan Cruyff ArenA et qui accueille
                les
                grands concerts d'artistes de renommée internationale. Il accueille également de nombreux festivals de
                musique électronique tel que l'Amsterdam Music Festival, notamment avec les disc-jockeys néerlandais
                Armin
                van Buuren, Hardwell, Martin Garrix et Tiësto. Toujours à proximité de l'Amsterdam ArenA, le Ziggo Dome
                ouvre ses portes en 2012, et accueille des artistes internationaux comme Pearl Jam, Madonna, Beyoncé ou
                encore Lady Gaga. Le Paradiso est une salle de spectacle et un centre culturel situés dans une ancienne
                église d'Amsterdam, bâtie entre 1879 et 1880 près de la Leidseplein, l'un des centres touristiques et
                culturels de la ville. Également situé près du Leidseplein, la Melkweg est un autre lieu alternatif
                multi-disciplinaire, né d'une organisation indépendante en 1970. Tous deux offrent une programmation
                éclectique allant du rock indépendant au hip-hop, en passant par le R'n'B ou et d'autres genres
                populaires.
                Parmi les autres lieux de musique plus axés sur les sous-cultures, on peut notamment citer les salles
                OCCII,
                OT301, De Nieuwe Anita, Winston-Uni et Zaal 100. Chaque printemps, se déroule le festival 5 Days Off qui
                est
                hébergé pendant cinq soirs au Paradiso et au Melkweg. Pendant l'été, plusieurs grands concerts se
                produisent
                en plein air tels que A Day at the Park.
            </p>

            <h3>Théâtre et cabaret</h3>
            <p> La ville d'Amsterdam accueille plusieurs lieux d'expression théâtrale. Bâtiment néo-Renaissance
                construit en
                1894 sur la Leidseplein, le Stadsschouwburg héberge la compagnie du Toneelgroep Amsterdam. Alors que la
                plupart des pièces étaient jusqu'alors jouées dans la grande salle, le bâtiment a subi une importante
                phase
                de rénovation et d'expansion pour créer une salle de représentation supplémentaire qui est opérée
                conjointement avec le Melkweg. Le théâtre royal Carré, construit sur les rives de l'Amstel en 1887 dans
                le
                même style néo-Renaissance en vogue à l'époque, avait vocation initiale d'héberger un cirque permanent.
                Il
                est désormais l'hôte de spectacles de cabaret, de comédies musicales et de quelques concerts. Le Théâtre
                Tuschinski et la réouverture récente de la salle DeLaMar permet de compléter l'offre en ce qui concerne
                les
                pièces de théâtre et les comédies musicales.
                <br><br>
                Les Pays-Bas possèdent une forte tradition de cabaret, qui combine à la fois la musique, les contes, les
                commentaires, le théâtre et la comédie. Le genre du cabaret remonte aux années 1930 où des artistes
                comme
                Wim Kan, Wim Sonneveld et Toon Hermans ont été les pionniers de cette forme d'art aux Pays-Bas. On
                trouve
                ainsi, à Amsterdam, une académie des arts de la scène spécialement consacrée au cabaret, la
                Kleinkunstacademie. Parmi les artistes populaires contemporains se trouvent, par exemple, Freek de
                Jonge,
                Herman Finkers, Hans Teeuwen, Herman van Veen, Youp van 't Hek, Theo Maassen, Najib Amhali, Raoul
                Heertje,
                Jörgen Raymann, Brigitte Kaandorp et Comedytrain.

            </p>
            <h3>Littérature</h3>
            <p> L'action de nombreuses œuvres littéraires renommées se déroule totalement ou partiellement à Amsterdam.
                L'une des plus universellement reconnues est Le Journal d'Anne Frank, livre composé du journal intime
                tenu
                par Anne Frank, une jeune juive allemande exilée aux Pays-Bas, lorsqu'elle se cache au-dessus d'un
                magasin
                situé près de Westerkerk, pendant vingt-cinq mois, avec sa famille et quatre amis, au cours de
                l'occupation
                des Pays-Bas par l'Allemagne nazie. Dans le roman La Chute d'Albert Camus (1956), l'histoire du
                principal
                protagoniste, Jean-Baptiste Clamence, se déroule à Amsterdam, où l'on apprend qu'il s'est exilé.

                <br><br>
                L'écrivain néerlandais Cees Nooteboom choisit également Amsterdam comme décor de son roman Rituels, paru
                en
                1983 et qui raconte l'histoire de deux amis dont l'un a la fâcheuse tendance à violer la loi, tandis que
                l'autre s'y plie avec discipline. La ville est également le théâtre du roman De hoogste tijd de Harry
                Mulisch, paru en 1985, et qui brosse un portrait détaillé de la ville moderne en racontant l'histoire de
                l'acteur néerlandais classique Pierre de Vries. Dans Sur l'eau, paru en 1998, H. M. van den Brink
                raconte
                l'histoire de deux rameurs d'un club nautique de l'Amstel, Anton et David qui est juif ; ces derniers
                voient
                alors leur destin basculer au moment de l'invasion allemande.
                <br><br>

                Dans Le Ministère de la Douleur, sorti en 2005, Dubravka Ugrešić dépeint les conditions de vie
                difficiles
                des immigrés d'Europe de l'Est dans l'un des quartiers pauvres d'Amsterdam, la ville ayant toujours
                constitué un centre d'accueil pour les réfugiés.
            </p>
            <h3>Cinéma</h3>
            <p> Amsterdam sert de décor à de nombreux films et séries télévisées, à la fois néerlandais et
                internationaux.
                Parmi les films néerlandais qui mettent en scène la ville est notamment connu à l'étranger Le Choix du
                destin (Soldaat van Oranje en néerlandais), réalisé par Paul Verhoeven et sorti en 1977. Le film, dans
                lequel joue entre autres Rutger Hauer, raconte l'histoire de six étudiants de l'université de Leyde, à
                l'approche de la Seconde Guerre mondiale. Insouciants au début du film, la guerre va changer leur vie ;
                alors que certains vont choisir la rébellion et résister à l'occupant, d'autres vont opter pour la
                collaboration. Ciske le Filou, sorti en 1984 raconte l'histoire de Ciske, un enfant de 11 ans vivant
                dans un
                quartier pauvre d'Amsterdam dans les années 1930. Le film est inspiré d'un roman pour enfants ; l'acteur
                Danny de Munk y interprète une chanson, « Je me sens tellement seul »Note 4, devenue un morceau
                classique
                pour la ville d'Amsterdam. Toujours dans les années 1980, le film L'Assaut, adaptation cinématographique
                du
                roman De Aanslag de Harry Mulisch, raconte l'histoire d'Anton Steenwijk qui essaie de comprendre les
                circonstances de la mort de sa famille dans une attaque allemande au cours de la Seconde Guerre
                mondiale.
                Bien que l'action se déroule principalement à Haarlem, le film, oscarisé en 1987, constitue une
                référence du
                cinéma néerlandais. Dans Amsterdamned, réalisé par Dick Maas et sorti en 1988, un dangereux plongeur
                sévit
                dans la ville, tuant sauvagement ses victimes à coups de couteau cranté. Le tueur utilise ainsi les
                canaux
                de la ville pour commettre ses crimes.
                <br><br>
                Amsterdam est également apparue dans plusieurs grosses productions hollywoodiennes et internationales.
                Dans
                Les diamants sont éternels, sorti en 1971, James Bond, interprété par Sean Connery, se rend dans la
                capitale
                néerlandaise pour y rencontrer Tiffany Case ; le film met principalement en scène les canaux de la
                ville, en
                particulier le Magere Brug. Deux ans plus tard, dans Turkish Délices, la ville est le théâtre principal
                de
                la relation passionnée et tumultueuse d'Éric, sculpteur bohème, avec Olga, issue d'une famille
                conservatrice. Le film met ainsi en scène de nombreuses parties de la ville, comme la place du Dam et le
                Damrak, le Rokin, l'Oudezijds Voorburgwal et le Vondelpark. Dans le film Pulp Fiction de Quentin
                Tarantino,
                Vincent Vega (interprété par John Travolta) revient à Los Angeles après avoir passé trois ans à
                Amsterdam.
                La scène d'ouverture du film Ocean's Twelve, réalisé par Steven Soderbergh, montre l'équipe de Daniel
                Ocean
                en train d'organiser un casse à Amsterdam. Pour ce faire, les bandits vont jusqu'à faire descendre une
                maison de plusieurs centimètres en affaissant les pilotis sur lesquels elle est construite. La comédie
                américaine de 2005 Gigolo malgré lui se déroule à Amsterdam et montre les méfaits de la consommation de
                cannabis et la prostitution, mais aborde également la thématique du racisme.
                <br><br>
                Plus récemment, quelques scènes de Nos étoiles contraires (2014), film racontant l'histoire de deux
                adolescents atteints par le cancer, se déroulent à Amsterdam. Dans Hitman and Bodyguard (2017), une
                course-poursuite prend place à Amsterdam, notamment devant le Rijksmuseum ; d'autres scènes du film sont
                tournées à La Haye, mettant en vue la Cour pénale internationale (CPI).
            </p>
            <h3>Musiques et chansons</h3>

            <p>
                La chanson Amsterdam, interprétée par Jacques Brel, est l'une des chansons francophones consacrées à la
                ville les plus illustres. Le titre a souvent été repris, comme par le groupe Oi! orléanais Komintern
                Sect,
                et notamment en anglais par Scott Walker, David Bowie et John Cale. Il a également été repris et modifié
                par
                le groupe Parabellum, qui en a fait une chanson contre l'usage des drogues. D'autres artistes
                francophones
                ont également chanté la « Venise du Nord » (surnom également donné à la ville de Bruges en Belgique), on
                peut ainsi citer Guy Béart (À Amsterdam), Maxime Le Forestier (Petit Nuage sur Amsterdam), Les Innocents
                (Entre Amos et Amsterdam), Graziella de Michele (Vision d'Amsterdam), Billy Ze Kick (Bons Baisers
                d'Amsterdam) ou encore Oxmo Puccino (Sur la Route d'Amsterdam). Plus récemment, le groupe de rap Octobre
                Rouge a également rendu hommage à la ville et plus particulièrement à son fameux quartier rouge dans le
                titre Week end a Meda.
                <br><br>
                Dans un registre international, plusieurs chansons baptisées Amsterdam ont été interprétées par des
                artistes
                comme la chanteuse néerlandaise Maggie MacNeal, ou les groupes Coldplay, Van Halen, Peter Bjorn and John
                ou
                encore Mando Diao. Les chansons « Aan de Amsterdamse grachten » (« Aux canaux d'Amsterdam ») de Pieter
                Goemans ou encore « Tulips from Amsterdam » du chanteur britannique Max Bygraves sont également devenues
                des
                classiques populaires.
            </p>
